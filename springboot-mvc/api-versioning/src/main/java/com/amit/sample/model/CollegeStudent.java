package com.amit.sample.model;

public class CollegeStudent {

	private Name name;

	  public CollegeStudent() {
	    super();
	  }

	  public CollegeStudent(Name name) {
	    super();
	    this.name = name;
	  }

	  public Name getName() {
	    return name;
	  }

	  public void setName(Name name) {
	    this.name = name;
	  }
}
