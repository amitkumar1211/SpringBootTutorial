package com.amit.sample.model;

public class SchoolStudent {

	private String name;

	public SchoolStudent() {
		super();
	}

	public SchoolStudent(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
